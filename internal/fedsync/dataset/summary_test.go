func TestBuildSummary(t *testing.T) {
	summary := BuildSummary(nil)

	require.Equal(t, 42, summary.Total)
	require.Equal(t, []Count{
		{Key: "1", Count: 12},
		{Key: "1b", Count: 6},
		{Key: "2", Count: 15},
		{Key: "3", Count: 9},
	}, summary.ByPhase)
	require.Equal(t, []Count{
		{Key: "daily", Count: 4},
		{Key: "weekly", Count: 2},
		{Key: "monthly", Count: 15},
		{Key: "quarterly", Count: 7},
		{Key: "annual", Count: 14},
	}, summary.ByCadence)
}

func TestRenderMarkdownSummary_MatchesDocs(t *testing.T) {
	expected := RenderMarkdownSummary(nil)

	for _, relPath := range []string{
		filepath.Join("..", "..", "..", "README.md"),
		filepath.Join("..", "..", "..", "AGENTS.md"),
	} {
		content, err := os.ReadFile(relPath)
		require.NoError(t, err)
		require.Equal(t, expected, extractSummaryBlock(t, string(content)), relPath)
	}
}

func TestBuildCatalog(t *testing.T) {
	catalog, err := BuildCatalog(nil)
	require.NoError(t, err)
	require.Equal(t, 42, catalog.Total)
	require.Len(t, catalog.Datasets, 42)
	require.Equal(t, "County Business Patterns", catalog.Datasets[0].Label)
	require.NotEmpty(t, catalog.Datasets[0].Description)
}

