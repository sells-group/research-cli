func TestReplaceSummaryBlock(t *testing.T) {
	content := "before\n" + SummaryBlockStart + "\nold\n" + SummaryBlockEnd + "\nafter\n"

	updated, err := ReplaceSummaryBlock(content, RenderMarkdownSummary(nil))
	require.NoError(t, err)
	require.Contains(t, updated, "## Live Fedsync Dataset Summary")
	require.Contains(t, updated, "before\n")
	require.Contains(t, updated, "\nafter\n")
}

func extractSummaryBlock(t *testing.T, content string) string {
	t.Helper()

	start := strings.Index(content, SummaryBlockStart)
	require.NotEqual(t, -1, start, "missing summary start marker")

	end := strings.Index(content, SummaryBlockEnd)
	require.NotEqual(t, -1, end, "missing summary end marker")

	end += len(SummaryBlockEnd)
	return strings.TrimSpace(content[start:end])
}
